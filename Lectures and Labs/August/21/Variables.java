class Variables{
    public static void main(String[] args) {
        int var1;
        double var2;

        var1 = 10;
        var2 = 10.0;

        System.out.println("Var1 contains " + var1);
        System.out.println("Var2 contains " + var2);    

        var1 = var1 / 2;
        var2 = var2 / 4;

        System.out.println("Var1 contains " + var1);
        System.out.println("Var2 contains " + var2);  
    }
}